# include <stdio.h>

int main() {
    int a = 2,b = 3;
    printf("Resultado: a > b: %d \n",a > b);
    printf("Resultado: a < b: %d \n",a < b);
    printf("Resultado: a >= b: %d \n",a >= b);
    printf("Resultado: a <= b: %d \n",a <= b);
    printf("Resultado: a == b: %d \n",a == b);
    printf("Resultado: a != b: %d \n",a != b);
    return 0;
}